            <h2>Koszta i produktywność</h2>
                <p>To hobby może być darmowe, lub czasem lekko szarpnąć kieszeń. Jest kilka narzędzi (o których w artykule poniżej), w które warto zainwestować, aby być bardziej produktywnym. Produktywność jest silnie zależna od tego jak dobrze potrafisz się uporać z problemami, jednak problematyka problem solvingu jest szersza.</p>

