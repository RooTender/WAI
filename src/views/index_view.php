            <h3>W skrócie</h3>
                <p><b>Nie wiesz – pytaj, szukaj, zgłębiaj wiedzę i próbuj.</b> Na tym polega mechanizm poznawania, a dzięki temu rozwijasz się, a także potrafisz szybciej i lepiej rozprawić się z problemami.</p>

                <p>No dobrze, a więc narzędzia. Posiadam niemałą kolekcję przydatnych materiałów czy programów, z których korzystam w swoim hobby.</p>
