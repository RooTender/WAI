            <h2>Słowem wstępu</h2>
                <p><strong>Problem Solving</strong> (pol. <em>Rozwiązywanie problemów</em>) polega na stosowaniu konkretnych metod, w celu znalezienia problemu. Korzystamy z niej podczas: naprawiania czegoś, prowadzenia dysputy, leczeniu choroby, a mówiąc ogólniej – problemu, z którym chcemy się uporać.</p>
                <p>Umiejętność ta opiera się ona na psychologii poznawczej (<i>kognitywnej</i>) i można ją rozwijać ćwicząc swój mózg łamigłówkami czy ćwiczeniami. Oczywiście nie muszą to być ćwiczenia, które się rozwiązuje z ołówkiem w ręku, ponieważ są też sytuacje, kiedy musimy stawiać czoła pod presją (mówiąc jaśniej – w ciężkich sytuacjach).</p>
                <p>Ludzie o wyższym poziomie tej umiejętności są często określani jako sprytni czy inteligentni ze względu na ich zdolności radzenia sobie w życiu. Ów spryt i inteligencję rozumiem, jako rzutkość w analizowaniu zagadnień i skuteczność stosowania w nich własnych rozwiązań.</p>
                <p>Jest wiele dziedzin życia, w których korzystamy ze wspomnianej wyżej umiejętności. Jako, że posiadam kilka zainteresowań często się pytam samego siebie:</p>
                <ul>
                    <li>Jak można zrobić to szybciej?</li>
                    <li>Czy jest można to uprościć? (Brzytwa Ockhama)</li>
                    <li>Czy istnieją jakieś zależności między tym zagadnieniem, a tym co wiem?</li>
                    <li>Czy jest to dobry moment na zajmowanie się tym problemem?</li>
                    <li>Jak dogłębnie muszę zagłębić się w napotkanej trudności?</li>
                </ul>
