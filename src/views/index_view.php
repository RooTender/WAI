            <h2>Poradnik korzystania z Internetu (nie tylko dla seniorów)</h2>
            <p>Nawiązując do poprzedniego artykułu jest wciąż wiele osób, które nie potrafią poprawnie korzystać z wyszukiwarek internetowych. Sprawa wydaje się być prosta, a wcale nie musi tak być.</p>
            <p>Na pewno miałeś/aś kiedyś taką sytuację, że po kilku dobrych minutach (czy godzinach?!) szukania odpowiedzi na swoje pytanie dalej się nic nie wyświetla. Pozostaje tylko gorycz i frustracja.</p>
            <ol>
                <li>
                    <h4>Sprecyzuj to co szukasz</h4>
                    <p>Zastanów się. Na spokojnie. Skonkretyzuj to co jest Ci potrzebne i postaraj się ograniczyć sens tego o czym myślisz do maksymalnie pięciu słów kluczowych.</p>
                </li>
                <li>
                    <h4>Stosuj frazy</h4>
                    <p>Jeśli nie możesz czegoś znaleźć, spróbuj wyszukiwania używając odpowiednich fraz. Nie musi być to zwięzłe zdanie. Wystarczy przykładowo: <span class="expressText">Error 500 HTML</span></p>
                </li>
                <li>
                    <h4>Nie bój się kolejnych stron</h4>
                    <p>Jeśli nie możesz czegoś znaleźć na pierwszej stronie wyszukiwarki, zajrzyj do drugiej albo do trzeciej, itp. Istnieje możliwość, że odpowiedź na Twoje pytanie znajduje się na jakiejś stronie z niezoptymalizowanym SEO. Przyjmij taką możliwość.</p>
                </li>
                <li>
                    <h4>Google Hacking</h4>
                    <p>Woah! Spokojnie! Nie mamy przecież zamiaru się włamywać. Okazuje się, że przy wyszukiwaniu haseł można stosować odpowiednie zapytania do wyszukiwarek, które mogą pomóc w szybszym wyszukiwaniu informacji. Jednym z takich tricków jest wyszukiwanie wyrazu w cudzysłowu. Spowoduje to, że przeglądarka nie będzie uprawniona do pominięcia danego hasła.</p>
                </li>
                <li>
                    <h4>Uogólnij myśl</h4>
                    <p>Jeśli do tej pory niczego Ci się nie udało znaleźć to jest możliwość, że zbyt detalicznie podchodzisz do zagadnienia.</p>
                </li>
                <li>
                    <h4>Weź sprawy w swoje ręce</h4>
                    <p>Broń ostateczna. Wejdź na jakieś forum i zadaj pytanie. Pamiętaj o przestrzeganiu zasad. Jeśli nie wiesz gdzie jest najlepiej się zapytać to możesz to z własnej ciekawości wygooglować albo skorzystać z materiału z poprzedniego posta.</p>
