            <h2>Postaw na szybkość</h2>
            <p>Wyszukiwanie informacji w dzisiejszych czasach stało się prostą czynnością. Wyciągasz telefon, wchodzisz w gogle, wpisujesz frazę i wyskakują jakieś wyniki. Jednak czy zawsze są one trafne?</p>
            <p>No właśnie, znasz odpowiedź. O to kilka stron, które zaoszczędzą Twój przyszły czas, jeśli byś zabłądził/a w Internecie.</p>
            <ol>
                <li>
                    <h4>Stack Overflow</h4>
                    <p>Serwis społecznościowy stworzony z myślą o programistach. Niezwykle użyteczny w problemach z kodem, zrozumienia logiki biznesowej aplikacji czy odnalezienia odpowiedniego algorytmu.</p>
                </li>
                <li>
                    <h4>Stack Exchange (Network)</h4>
                    <p>Sieć stron internetowych, gdzie każda z nich jest poświęcona konkretnemu tematowi. Wspomniany wcześniej Stack Overflow należy również do tej sieci. Jest ona uniwersalnym rozwiązaniem na większość pytań internautów.</p>
                </li>
                <li>
                    <h4>Quora</h4>
                    <p>Serwis Q&A (<i>Questions and Answers</i>) działający na podobnej zasadzie co strony wymienione wyżej. Pytania jednak muszą być krótkie i konkretne, a odpowiedzi wysokiej jakości. Treść pytań jest dokładnie monitorowana pod tym względem przez moderatorów.</p>
                </li>
                <li>
                    <h4>DevTo</h4>
                    <p>Strona od programistów, dla programistów. Społeczność dzieli się tu opiniami, spostrzeżeniami, ciekawostkami i inspiracjami. Można znaleźć tu wiele wartościowych treści oraz wsparcia, kiedy natrafisz na ścianę w swoim projekcie.</p>
                </li>
                <li>
                    <h4>Reddit</h4>
                    <p>Portal społecznościowy, w którym każdy może wnieść coś od siebie. Panuje tu swoboda wypowiadania się, jak i możliwości luźnych konwersacji w komentarzach. Warto z niej korzystać w przypadku pytań małej wagi.</p>
                </li>
            </ol>

